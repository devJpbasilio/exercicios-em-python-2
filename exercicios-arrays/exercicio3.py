import numpy as np

# Dado o array a = np.array([1, 2, 3, 4, 5]), calcule:
#
# O quadrado de cada elemento
# A soma dos elementos
# A média dos elementos
# O maior e o menor valor

a = np.array([1, 2, 3, 4, 5])

print(a)
print(np.sum(a)) #Somando
print(np.mean(a))#Média
print(np.max(a)) #Maior dos elementos
print(np.min(a)) #Menor dos elementos
print(np.sqrt(a)) #Raiz Quadrada
